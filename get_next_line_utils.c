#include "get_next_line.h"

size_t	ft_strlen_ult(char *s)
{
	size_t i;
    
    i = 0;
	if (s == NULL)
		return (0);
	while (s[i] != '\0' && s[i] != '\n')
		i++;
	return (i);
}
